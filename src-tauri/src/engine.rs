/// Generate a still image (PNG bytes) with an installed image model.
pub fn generate_image(
    s: &AppSettings,
    prompt: &str,
    model: &str,
    width: u32,
    height: u32,
    steps: u32,
    lora: Option<&str>,
    seed: i64,
) -> Result<Vec<u8>, String> {
    let mut body = json!({
        "prompt": prompt,
        "model": model,
        "kind": "promo",
        "width": width,
        "height": height,
        "steps": steps,
        "seed": seed,
    });
    if let Some(l) = lora {
        if !l.is_empty() {
            body["lora"] = json!(l);
        }
    }
    run_job(s, "image", body)
}

/// Edit an existing image (PNG/JPEG bytes) with a text instruction using an
/// installed edit-capable model (e.g. Qwen-Image-Edit). The source image is
/// sent as a base64 data URL in `reference_image`.
pub fn edit_image(
    s: &AppSettings,
    image: &[u8],
    prompt: &str,
    model: &str,
    steps: u32,
    seed: i64,
) -> Result<Vec<u8>, String> {
    let b64 = base64::engine::general_purpose::STANDARD.encode(image);
    let body = json!({
        "prompt": prompt,
        "model": model,
        "kind": "edit",
        "reference_image": format!("data:image/png;base64,{b64}"),
        "steps": steps,
        "seed": seed,
    });
    run_job(s, "image", body)
}

/// Generate a video clip (MP4 bytes) with an installed video model. When
/// `image_png` is provided it is sent as an image-to-video reference (base64
/// data URL); otherwise the call is pure text-to-video.
pub fn generate_video(
    s: &AppSettings,
    prompt: &str,
    model: &str,
    mode: &str,
    width: u32,
    height: u32,
    num_frames: u32,
    fps: u32,
    image_png: Option<&[u8]>,
    lora: Option<&str>,
    seed: i64,
) -> Result<Vec<u8>, String> {
    let mut body = json!({
        "prompt": prompt,
        "model": model,
        "mode": mode,
        "width": width,
        "height": height,
        "num_frames": num_frames,
        "fps": fps,
        "seed": seed,
    });
    if let Some(png) = image_png {
        let b64 = base64::engine::general_purpose::STANDARD.encode(png);
        body["image"] = json!(format!("data:image/png;base64,{b64}"));
    }
    if let Some(l) = lora {
        if !l.is_empty() {
            body["lora"] = json!(l);
        }
    }
    run_job(s, "video", body)
}

/// Re-light a clip (MP4 bytes) to match a 360 HDRI environment map using the
/// NVIDIA Relight runtime. `video` + `environment_map` are sent as base64 data
/// URLs so the engine never needs shared storage. `intensity` is 0..1.
pub fn relight_video(
    s: &AppSettings,
    video: &[u8],
    environment_map: &[u8],
    env_filename: &str,
    intensity: f64,
    fps: u32,
    seed: i64,
) -> Result<Vec<u8>, String> {
    let vid_b64 = base64::engine::general_purpose::STANDARD.encode(video);
    // Pick an image mime from the HDRI extension (most are .hdr/.exr/.png/.jpg).
    let env_mime = match env_filename.rsplit('.').next().unwrap_or("").to_ascii_lowercase().as_str() {
        "png" => "image/png",
        "jpg" | "jpeg" => "image/jpeg",
        "exr" => "image/x-exr",
        "webp" => "image/webp",
        _ => "image/x-hdr", // .hdr / radiance
    };
    let env_b64 = base64::engine::general_purpose::STANDARD.encode(environment_map);
    let body = json!({
        "model": "relight",
        "video": format!("data:video/mp4;base64,{vid_b64}"),
        "environment_map": format!("data:{env_mime};base64,{env_b64}"),
        "intensity": intensity.clamp(0.0, 1.0),
        "fps": fps.clamp(8, 60),
        "seed": seed,
    });
    run_job_at(s, "/video/relight", "video", body)
}

/// Generate a music track (WAV bytes) with an installed ACE-Step model.
pub fn generate_music(
    s: &AppSettings,
    prompt: &str,
    model: &str,
    kind: &str,
    duration_seconds: i64,
    lyrics: Option<&str>,
    lora: Option<&str>,
    seed: i64,
) -> Result<Vec<u8>, String> {
    let mut body = json!({
        "prompt": prompt,
        "model": model,
        "kind": kind,
        "duration_seconds": duration_seconds.clamp(4, 300),
        "format": "wav",
        "seed": seed,
    });
    if let Some(l) = lyrics {
        if !l.is_empty() {
            body["lyrics"] = json!(l);
        }
    }
    if let Some(l) = lora {
        if !l.is_empty() {
            body["lora"] = json!(l);
        }
    }
    run_job(s, "music", body)
}

