//! Boostify AI Engine client — talks to the private FastAPI inference server
//! (running on the H200/H100) to generate images (FLUX / Qwen), video
//! (LTX-2.3 / Wan2.2) and music (ACE-Step) with the artist's own installed
//! models.
//!
//! The engine exposes an async job API:
//!   * `POST /{domain}/generate`  -> `{ job_id, status_url }`
//!   * `POST /{domain}/status` `{job_id}` -> `JobStatus { completed, failed, … }`
//!   * output files are served at `/files/{domain}/{job_id}/{file}`
//! authenticated with the `x-api-key` header.
//!
//! This module hides that flow behind simple blocking helpers that return the
//! generated bytes, so the video-editor orchestration in `lib.rs` can swap the
//! NVIDIA cloud / local Ken Burns path for the real installed models whenever an
//! engine URL is configured in Settings. It is deliberately pure: no DB access.

use std::io::Read;
use std::time::Duration;

use base64::Engine as _;
use serde_json::{json, Value};

use crate::models::*;

/// Seconds between status polls.
const POLL_SECS: u64 = 4;
/// Max polls (~15 min) — heavy video generation can be slow.
const POLL_MAX: u32 = 225;

/// Resolve the engine base URL from settings (trimmed, no trailing slash).
/// Returns `None` when no engine is configured.
pub fn base_url(s: &AppSettings) -> Option<String> {
    let u = s.ai_engine_url.trim().trim_end_matches('/').to_string();
    if u.is_empty() {
        None
    } else {
        Some(u)
    }
}

fn api_key(s: &AppSettings) -> String {
    s.ai_engine_key.trim().to_string()
}

/// True when an engine URL is set (the app should prefer installed models).
pub fn is_configured(s: &AppSettings) -> bool {
    base_url(s).is_some()
}

fn post(base: &str, key: &str, path: &str, body: Value, timeout: u64) -> Result<Value, String> {
    let mut req = ureq::post(&format!("{base}{path}"))
        .set("Accept", "application/json")
        .set("Content-Type", "application/json")
        .timeout(Duration::from_secs(timeout));
    if !key.is_empty() {
        req = req.set("x-api-key", key);
    }
    match req.send_json(body) {
        Ok(r) => r.into_json().map_err(|e| format!("bad json: {e}")),
        Err(ureq::Error::Status(code, r)) => {
            let detail = r
                .into_string()
                .unwrap_or_default()
                .chars()
                .take(300)
                .collect::<String>();
            Err(format!("HTTP {code}: {detail}"))
        }
        Err(e) => Err(format!("request failed: {e}")),
    }
}

fn get_json(base: &str, key: &str, path: &str, timeout: u64) -> Result<Value, String> {
    let mut req = ureq::get(&format!("{base}{path}")).timeout(Duration::from_secs(timeout));
    if !key.is_empty() {
        req = req.set("x-api-key", key);
    }
    match req.call() {
        Ok(r) => r.into_json().map_err(|e| format!("bad json: {e}")),
        Err(ureq::Error::Status(code, r)) => {
            let detail = r.into_string().unwrap_or_default();
            Err(format!("HTTP {code}: {detail}"))
        }
        Err(e) => Err(format!("request failed: {e}")),
    }
}

fn get_bytes(base: &str, key: &str, path: &str) -> Result<Vec<u8>, String> {
    let mut req = ureq::get(&format!("{base}{path}")).timeout(Duration::from_secs(180));
    if !key.is_empty() {
        req = req.set("x-api-key", key);
    }
    match req.call() {
        Ok(r) => {
            let mut buf = Vec::new();
            r.into_reader()
                .read_to_end(&mut buf)
                .map_err(|e| format!("read failed: {e}"))?;
            if buf.is_empty() {
                Err("empty file".into())
            } else {
                Ok(buf)
            }
        }
        Err(e) => Err(format!("download failed: {e}")),
    }
}

/// Run a `generate` job for a domain and return the first output's bytes.
fn run_job(s: &AppSettings, domain: &str, body: Value) -> Result<Vec<u8>, String> {
    run_job_at(s, &format!("/{domain}/generate"), domain, body)
}

/// Like `run_job` but lets the caller pick the submit path (e.g. `/video/relight`)
/// while still polling `/{status_domain}/status` and downloading from `/files/…`.
fn run_job_at(
    s: &AppSettings,
    gen_path: &str,
    status_domain: &str,
    body: Value,
) -> Result<Vec<u8>, String> {
    let base = base_url(s).ok_or("AI engine URL not set")?;
    let key = api_key(s);

    let accepted = post(&base, &key, gen_path, body, 120)
        .map_err(|e| format!("engine {status_domain} generate: {e}"))?;
    let job_id = accepted
        .get("job_id")
        .and_then(|v| v.as_str())
        .ok_or("engine returned no job_id")?
        .to_string();
    let domain = status_domain;

    for _ in 0..POLL_MAX {
        std::thread::sleep(Duration::from_secs(POLL_SECS));
        let st = match post(
            &base,
            &key,
            &format!("/{domain}/status"),
            json!({ "job_id": job_id }),
            60,
        ) {
            Ok(v) => v,
            Err(_) => continue, // transient — keep polling
        };

        if st.get("failed").and_then(|v| v.as_bool()).unwrap_or(false) {
            let err = st
                .get("error")
                .and_then(|v| v.as_str())
                .unwrap_or("unknown");
            return Err(format!("engine {domain} job failed: {err}"));
        }

        if st.get("completed").and_then(|v| v.as_bool()).unwrap_or(false) {
            // Preferred: an absolute-ish output_url like /files/{domain}/{job_id}/{file}.
            if let Some(url) = st.get("output_url").and_then(|v| v.as_str()) {
                if !url.is_empty() {
                    if let Ok(b) = get_bytes(&base, &key, url) {
                        return Ok(b);
                    }
                }
            }
            // Fallback: reconstruct from output_files (relative names).
            if let Some(files) = st.get("output_files").and_then(|v| v.as_array()) {
                if let Some(f) = files.first().and_then(|v| v.as_str()) {
                    let url = format!("/files/{domain}/{job_id}/{f}");
                    if let Ok(b) = get_bytes(&base, &key, &url) {
                        return Ok(b);
                    }
                }
            }
            return Err(format!("engine {domain} completed but no downloadable output"));
        }
    }
    Err(format!("engine {domain} job timed out"))
}

