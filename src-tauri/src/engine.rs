fn label_for(id: &str, description: &str) -> String {
    if !description.is_empty() {
        description.to_string()
    } else {
        id.to_string()
    }
}

/// Probe the engine: is it configured, reachable, and what models does it serve.
pub fn status(s: &AppSettings) -> EngineStatus {
    let base = match base_url(s) {
        Some(b) => b,
        None => {
            return EngineStatus {
                configured: false,
                reachable: false,
                base_url: String::new(),
                models: vec![],
                message: "Set the AI Engine URL in Settings to use your installed models."
                    .into(),
            };
        }
    };
    let key = api_key(s);

    // /health is unauthenticated.
    let health = get_json(&base, "", "/health", 8);
    if let Err(e) = health {
        return EngineStatus {
            configured: true,
            reachable: false,
            base_url: base.clone(),
            models: vec![],
            message: format!("Engine not reachable at {base}: {e}"),
        };
    }
    let sim = health
        .as_ref()
        .ok()
        .and_then(|v| v.get("simulate").and_then(|x| x.as_bool()))
        .unwrap_or(false);

    // /models requires the API key.
    let mut models: Vec<EngineModel> = vec![];
    match get_json(&base, &key, "/models", 10) {
        Ok(v) => {
            if let Some(map) = v.get("models").and_then(|m| m.as_object()) {
                for (domain, items) in map {
                    if let Some(arr) = items.as_array() {
                        for it in arr {
                            if let Some(id) = it.get("id").and_then(|x| x.as_str()) {
                                let desc = it
                                    .get("description")
                                    .and_then(|x| x.as_str())
                                    .unwrap_or("");
                                models.push(EngineModel {
                                    id: id.to_string(),
                                    domain: domain.clone(),
                                    label: label_for(id, desc),
                                });
                            }
                        }
                    }
                }
            }
        }
        Err(e) => {
            return EngineStatus {
                configured: true,
                reachable: true,
                base_url: base.clone(),
                models: vec![],
                message: format!("Engine reachable but /models failed (check API key): {e}"),
            };
        }
    }

    let msg = if models.is_empty() {
        "Engine reachable but no models advertised.".to_string()
    } else if sim {
        format!("Engine connected (SIMULATION mode) — {} models.", models.len())
    } else {
        format!("Engine connected — {} installed models.", models.len())
    };

    EngineStatus {
        configured: true,
        reachable: true,
        base_url: base,
        models,
        message: msg,
    }
}
